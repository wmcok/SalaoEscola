SELECT * FROM Lancamentos
SELECT * FROM Usuarios
SELECT * FROM Naturezas
SELECT * FROM Produtos
SELECT * FROM Filial
SELECT * FROM LancamentosProdutos
SELECT CASE F.TIPO WHEN 0 THEN 'PAGAR' WHEN 1 THEN 'RECEBER' END TIPO,* FROM Financeiro F 
INNER JOIN NATUREZAS N ON N.Codigo=F.CodNatureza
INNER JOIN FILIAL FI ON FI.Codigo=F.CodFilial
INNER JOIN USUARIOS U ON U.CODIGO = F.CodUsuario
WHERE F.Vencimento BETWEEN '2020-01-01' AND  '2020-03-31' 
AND F.DataPagamento  BETWEEN '2020-01-01' AND  '2020-03-31'
AND F.CodLancamento = ''
AND F.Codigo = ''
AND F.CodNatureza = ''
and F.Tipo IN ('PAGAR','RECEBER')

